# WAP program to print day name of week

# Input week number from user 

weekDayNumber=int(input("Enter week number (1-7): "))
if(weekDayNumber == 1):
    print("Monday")   
elif(weekDayNumber == 2):   
    print("Tuesday")
    
elif(weekDayNumber == 3):
    
    print("Wednesday")
    
elif(weekDayNumber == 4):
    
    print("Thursday")
    
elif(weekDayNumber == 5):
    
    print("Friday")
    
elif(weekDayNumber == 6):
    
    print("Saturday")
    
elif(weekDayNumber == 7):
    
    print("Sunday")
    
else:
    
    print("Invalid Input! Please enter week number between 1-7.")
    